#include "./fdf.h"

int		line_fdf(char *str)
{
	int		ind;

	ind = 1;
	while (str[ind] != '.')
	{
		ind++;
	}
	if (str[ind] == '.' && str[ind + 1] == 'f' && str[ind + 2] == 'd' &&
		str[ind + 3] == 'f')
		return (1);
	return (0);
}
